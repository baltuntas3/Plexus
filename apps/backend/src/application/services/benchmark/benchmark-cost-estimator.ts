import type { BenchmarkCostForecast } from "../../../domain/entities/benchmark.js";
import type { PromptVersionSummary } from "../../queries/prompt-query-service.js";
import { calculateCost } from "../model-registry.js";
import { mean } from "../../utils/statistics.js";

// Cost forecasting for a benchmark configuration. Lives in the application
// layer because pricing comes from the model registry (a provider-aware
// infrastructure-adjacent port), but the output shape is a pure domain VO
// the aggregate carries around. Extracted from the create-benchmark use
// case so start-benchmark and update-test-cases stop importing from a
// peer use-case file.
//
// Inputs are described by a `testCount` and an `avgInputTokens` rather than
// a list of strings. This split lets callers compute a forecast BEFORE the
// test cases have been generated by the LLM (pre-flight gate) using a stub
// average, so a config that blows the budget is rejected without paying for
// the generator call. Callers that already have real inputs (start, edit)
// pass an empirical average computed via `averageTokenCount`.

// Conservative-but-not-extreme stub for pre-flight forecasts. Picked from
// observed generator output in production: typical user-turn inputs land in
// the 80–200 token range; 150 sits in the middle so the gate rejects
// clearly-over-budget configs without wrongly rejecting borderline ones.
// The post-generation forecast recomputes with real lengths and updates the
// stored estimate, so this number only affects the budget gate.
export const STUB_AVG_INPUT_TOKENS = 150;

// Default per-benchmark spend cap when the caller does not specify one.
// Single source of truth for create-, start- and runner-side defaults so
// the three sites stay in sync.
export const DEFAULT_BUDGET_USD = 50;

// Fixed overhead for the judge prompt itself (BASE_JUDGE_PROMPT plus the
// task-type guidance, the BATCH_JUDGE_INSTRUCTIONS block, and the XML tag
// scaffolding around <input>/<reference>/<attempt label="...">). Empirical
// midpoint: the static templates total ~340 tokens once tokenised by the
// proxy counter below; rounding to 350 keeps the forecast on the safe side
// of provider-specific tokenisation differences.
const JUDGE_PROMPT_OVERHEAD_TOKENS = 350;
// Per-attempt wrapping cost inside the batched user message
// (`<attempt label="ATTEMPT_N">...</attempt>` plus newlines). Constant
// across batch sizes; multiplied by `repetitions` below.
const ATTEMPT_WRAPPER_TOKENS = 12;
// Output tokens per per-candidate rubric entry — one JSON object with
// three integer scores and a one-sentence reasoning. Holds across batch
// sizes since the judge writes one entry per ATTEMPT label, not one per
// call.
const JUDGE_OUTPUT_TOKENS_PER_CANDIDATE = 32;

export interface BenchmarkCostEstimatorInput {
  versions: readonly PromptVersionSummary[];
  testCount: number;
  avgInputTokens: number;
  solverModels: readonly string[];
  judgeModels: readonly string[];
  repetitions: number;
}

export class BenchmarkCostEstimator {
  estimate(input: BenchmarkCostEstimatorInput): BenchmarkCostForecast {
    const versionPrompts = input.versions.map((v) => v.executablePrompt);
    const avgSystemPromptTokens = mean(versionPrompts.map(estimateTokenCount));
    const avgUserInputTokens = input.avgInputTokens;
    const avgCandidateOutputTokens = Math.max(
      64,
      Math.round(avgUserInputTokens * 1.6),
    );
    const estimatedMatrixCells =
      input.testCount *
      input.versions.length *
      input.solverModels.length *
      input.repetitions;

    let estimatedCandidateCostUsd = 0;
    for (const solverModel of input.solverModels) {
      const perCell = calculateCost(
        solverModel,
        Math.round(avgSystemPromptTokens + avgUserInputTokens),
        avgCandidateOutputTokens,
      );
      estimatedCandidateCostUsd +=
        perCell.totalUsd *
        input.testCount *
        input.versions.length *
        input.repetitions;
    }

    // Batched judging: the runner issues ONE call per
    // (testCase × version × solver × judgeModel). All `repetitions`
    // candidate outputs of that triple share the call's static prompt
    // (judge system prompt + system_prompt_under_evaluation + input +
    // reference) and only the per-attempt wrapping plus the candidate
    // body scale with `repetitions`. The output scales linearly because
    // the judge writes one rubric entry per ATTEMPT label.
    const judgeCallsPerJudgeModel =
      input.testCount * input.versions.length * input.solverModels.length;
    const judgeFixedInputTokens = Math.round(
      JUDGE_PROMPT_OVERHEAD_TOKENS +
        avgSystemPromptTokens +
        avgUserInputTokens,
    );
    const judgePerAttemptInputTokens =
      avgCandidateOutputTokens + ATTEMPT_WRAPPER_TOKENS;
    const judgeInputTokensPerCall =
      judgeFixedInputTokens + judgePerAttemptInputTokens * input.repetitions;
    const judgeOutputTokensPerCall =
      JUDGE_OUTPUT_TOKENS_PER_CANDIDATE * input.repetitions;

    let estimatedJudgeCostUsd = 0;
    for (const judgeModel of input.judgeModels) {
      const perCall = calculateCost(
        judgeModel,
        judgeInputTokensPerCall,
        judgeOutputTokensPerCall,
      );
      estimatedJudgeCostUsd += perCall.totalUsd * judgeCallsPerJudgeModel;
    }

    const totalJudgeCalls =
      judgeCallsPerJudgeModel * input.judgeModels.length;

    return {
      estimatedMatrixCells,
      estimatedCandidateInputTokens: Math.round(
        (avgSystemPromptTokens + avgUserInputTokens) * estimatedMatrixCells,
      ),
      estimatedCandidateOutputTokens:
        avgCandidateOutputTokens * estimatedMatrixCells,
      estimatedJudgeInputTokens:
        judgeInputTokensPerCall * totalJudgeCalls,
      estimatedJudgeOutputTokens:
        judgeOutputTokensPerCall * totalJudgeCalls,
      estimatedCandidateCostUsd,
      estimatedJudgeCostUsd,
      estimatedTotalCostUsd: estimatedCandidateCostUsd + estimatedJudgeCostUsd,
    };
  }
}

// Empirical average token count helper for callers that already have real
// inputs (start-benchmark, update-test-cases). The token counter matches
// the one used internally by the estimator so pre-flight and post-flight
// forecasts share the same length model.
export const averageTokenCount = (texts: readonly string[]): number =>
  mean(texts.map(estimateTokenCount));

const estimateTokenCount = (text: string): number => {
  const matches = text.match(/[\p{L}\p{N}]+(?:['_-][\p{L}\p{N}]+)*|[^\s]/gu);
  return matches?.length ?? 0;
};
